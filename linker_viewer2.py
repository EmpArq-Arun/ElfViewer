@app.route('/analyse_elf', method='POST')
def analyse_elf():
    response.content_type = 'application/json'
    try:
        elf_path = request.forms.get('elf_path', '').strip()
        tools    = json.loads(request.forms.get('tools', '{}'))
        ld_data  = json.loads(request.forms.get('ld_data', '{}'))

        if not elf_path or not os.path.isfile(elf_path):
            return json.dumps({"error": f"ELF file not found: {elf_path!r}"})

        # Resolve tool paths — check both bare name (on PATH) and full path
        def resolve(tool_name, default):
            t = tools.get(tool_name, default).strip()
            if shutil.which(t):
                return t
            # Try common install locations
            for prefix in ['/usr/bin/', '/usr/local/bin/', '/opt/arm/bin/']:
                if os.path.isfile(prefix + t.split('/')[-1]):
                    return prefix + t.split('/')[-1]
            return t  # Return as-is, will fail gracefully

        nm_tool  = resolve('nm',   'arm-none-eabi-nm')
        re_tool  = resolve('re',   'arm-none-eabi-readelf')
        sz_tool  = resolve('size', 'arm-none-eabi-size')

        # Run tools
        symbols     = parse_nm(nm_tool, elf_path)
        elf_secs    = parse_readelf_sections(re_tool, elf_path)
        size_data   = parse_size(sz_tool, elf_path)

        # Merge size data into elf_secs
        for name, s in size_data.items():
            if name in elf_secs:
                if elf_secs[name]['size'] == 0:
                    elf_secs[name]['size'] = s['size']
            else:
                elf_secs[name] = {'addr': s['addr'], 'size': s['size'],
                                  'type': 'PROGBITS', 'flags': ''}

        # Assign symbols to sections
        ld_sections = ld_data.get('sections', [])
        orphans = assign_symbols_to_sections(symbols, elf_secs, ld_sections)

        # Update region used_bytes
        for reg in ld_data.get('regions', []):
            reg['used_bytes'] = sum(
                elf_secs.get(s['name'], {}).get('size', 0)
                for s in reg['sections']
            )

        # Generate warnings
        warnings = generate_warnings(ld_data, elf_secs, symbols)

        # Startup cost
        startup = compute_startup_cost(ld_sections, elf_secs)

        return json.dumps({
            "symbols":      symbols,
            "elf_sections": elf_secs,
            "orphans":      orphans,
            "warnings":     warnings,
            "startup":      startup,
            "ld_data":      ld_data,
        })

    except Exception as ex:
        import traceback
        return json.dumps({"error": str(ex), "trace": traceback.format_exc()})

