def run_tool(args, timeout=15):
    """Run an external tool, return (stdout, stderr, returncode)."""
    try:
        r = subprocess.run(args, capture_output=True, text=True,
                          timeout=timeout, errors='replace')
        return r.stdout, r.stderr, r.returncode
    except FileNotFoundError:
        return '', f"Tool not found: {args[0]}", 1
    except subprocess.TimeoutExpired:
        return '', f"Timeout running: {' '.join(args)}", 1
    except Exception as e:
        return '', str(e), 1

def parse_nm(nm_path, elf_path):
    """
    Run nm --print-size --size-sort --radix=x on the ELF.
    Returns list of symbol dicts.
    """
    stdout, stderr, rc = run_tool([
        nm_path, '--print-size', '--radix=x', '--format=posix',
        '--special-syms', elf_path
    ])
    if rc != 0 or not stdout.strip():
        # Fallback without --special-syms
        stdout, stderr, rc = run_tool([
            nm_path, '--print-size', '--radix=x', '--format=posix', elf_path
        ])

    symbols = []
    for line in stdout.splitlines():
        line = line.strip()
        if not line: continue
        # posix format: name type value [size]
        parts = line.split()
        if len(parts) < 3: continue
        name = parts[0]
        ntype_raw = parts[1]
        try:
            addr = int(parts[2], 16)
            size = int(parts[3], 16) if len(parts) > 3 else 0
        except ValueError:
            continue

        ntype = NM_TYPE_MAP.get(ntype_raw, 'other')
        is_global = ntype_raw.isupper()

        # Skip internal/boring symbols
        if name.startswith('$') or name in ('', '_', '__'):
            continue

        symbols.append({
            "name":     name,
            "addr":     addr,
            "size":     size,
            "type":     ntype,
            "type_raw": ntype_raw,
            "global":   is_global,
            "color":    SYMBOL_COLORS.get(ntype, SYMBOL_COLORS['other']),
        })

    return sorted(symbols, key=lambda s: s['addr'])


def parse_readelf_sections(readelf_path, elf_path):
    """
    Run readelf -S to get section headers with addresses and sizes.
    Returns dict: section_name -> {addr, offset, size, type, flags}
    """
    stdout, _, rc = run_tool([readelf_path, '-S', '--wide', elf_path])
    sections = {}
    for line in stdout.splitlines():
        # [ Nr] Name  Type  Addr  Off  Size  ES Flg Lk Inf Al
        m = re.match(
            r'\s*\[\s*\d+\]\s+(\S+)\s+(\S+)\s+([0-9a-fA-F]+)\s+'
            r'([0-9a-fA-F]+)\s+([0-9a-fA-F]+)', line)
        if m:
            name  = m.group(1)
            stype = m.group(2)
            addr  = int(m.group(3), 16)
            size  = int(m.group(5), 16)
            flags_m = re.search(r'\d+\s+(\S*)\s+\d+\s+\d+\s+\d+\s*$', line)
            flags = flags_m.group(1) if flags_m else ''
            sections[name] = {"addr": addr, "size": size,
                              "type": stype, "flags": flags}
    return sections


def parse_size(size_path, elf_path):
    """Run size -A for per-section sizes."""
    stdout, _, rc = run_tool([size_path, '-A', '-x', elf_path])
    sizes = {}
    for line in stdout.splitlines():
        m = re.match(r'^(\S+)\s+(0x[0-9a-fA-F]+|\d+)\s+(0x[0-9a-fA-F]+|\d+)', line)
        if m:
            try:
                sizes[m.group(1)] = {'size': int(m.group(2),0), 'addr': int(m.group(3),0)}
            except:
                pass
    return sizes


def addr2line_lookup(addr2line_path, elf_path, addresses):
    """Bulk addr2line lookup. Returns dict addr(int) -> 'file:line'"""
    if not addresses:
        return {}
    addr_strs = [hex(a) for a in addresses]
    stdout, _, rc = run_tool(
        [addr2line_path, '-e', elf_path, '-f', '-C', '-p'] + addr_strs,
        timeout=30
    )
    result = {}
    lines = stdout.splitlines()
    for i, addr in enumerate(addresses):
        if i < len(lines):
            result[addr] = lines[i].strip()
    return result


def assign_symbols_to_sections(symbols, elf_sections, ld_sections):
    """
    Map each symbol to a linker section by address range.
    Also detect symbols that fall outside all known sections (linker bugs).
    """
    # Build sorted list of (start, end, sec_name) from ELF sections
    ranges = []
    for name, s in elf_sections.items():
        if s['size'] > 0:
            ranges.append((s['addr'], s['addr'] + s['size'], name))
    ranges.sort()

    sec_map = {s['name']: s for s in ld_sections}
    orphans = []

    for sym in symbols:
        if sym['addr'] == 0 or sym['type'] == 'undefined':
            continue
        matched = None
        for start, end, sname in ranges:
            if start <= sym['addr'] < end:
                matched = sname
                break
        sym['section'] = matched
        if matched and matched in sec_map:
            sec_map[matched]['symbols'].append(sym)
            sec_map[matched]['size'] = sec_map[matched].get('size', 0) + sym['size']
        elif matched is None:
            orphans.append(sym)

    return orphans


def generate_warnings(ld_data, elf_sections, symbols):
    """Generate actionable warnings for the Warnings tab."""
    warnings = []

    # 1. Section overflow
    for reg in ld_data['regions']:
        used = sum(elf_sections.get(s['name'], {}).get('size', 0) for s in reg['sections'])
        if reg['length'] > 0:
            pct = used / reg['length']
            if pct > 0.95:
                warnings.append({
                    "level": "error",
                    "category": "Memory Overflow",
                    "message": f"{reg['name']} is {pct*100:.1f}% full ({used:,} / {reg['length']:,} bytes)",
                    "detail": "Region is nearly full. Risk of linker error on next build."
                })
            elif pct > 0.80:
                warnings.append({
                    "level": "warn",
                    "category": "Memory Pressure",
                    "message": f"{reg['name']} is {pct*100:.1f}% full ({used:,} / {reg['length']:,} bytes)",
                    "detail": "Consider moving large constants to flash or enabling LTO."
                })

    # 2. DMA safety — large variables in cacheable SRAM
    cacheable_addrs = set()
    for sec in ld_data['sections']:
        if sec['cacheable']:
            es = elf_sections.get(sec['name'], {})
            if es:
                cacheable_addrs.add((es.get('addr',0), es.get('addr',0)+es.get('size',0)))

    dma_keywords = ['buf', 'buffer', 'frame', 'ipc', 'spi', 'dma', 'rx', 'tx', 'fifo']
    for sym in symbols:
        if sym['size'] >= 4 and sym['type'] in ('variable',):
            name_l = sym['name'].lower()
            if any(k in name_l for k in dma_keywords):
                for start, end in cacheable_addrs:
                    if start <= sym['addr'] < end:
                        warnings.append({
                            "level": "warn",
                            "category": "DMA Safety",
                            "message": f"'{sym['name']}' ({sym['size']} B) is in cacheable SRAM",
                            "detail": f"Address 0x{sym['addr']:08X}. If used with DMA, cache coherency "
                                      f"issues will cause silent data corruption. Move to .non_cacheable_bss."
                        })
                        break

    # 3. Large symbols eating RAM
    ram_syms = [s for s in symbols if s['size'] > 1024 and s['type'] in ('variable','common')]
    ram_syms.sort(key=lambda s: -s['size'])
    for sym in ram_syms[:5]:
        warnings.append({
            "level": "info",
            "category": "Large Symbol",
            "message": f"'{sym['name']}' uses {sym['size']:,} bytes of RAM",
            "detail": f"Address 0x{sym['addr']:08X}, section: {sym.get('section','?')}. "
                      f"Consider if this can be flash (const) or reduced in size."
        })

    # 4. Large functions (potential stack pressure)
    big_fns = [s for s in symbols if s['size'] > 4096 and s['type'] == 'function']
    big_fns.sort(key=lambda s: -s['size'])
    for sym in big_fns[:5]:
        warnings.append({
            "level": "info",
            "category": "Large Function",
            "message": f"'{sym['name']}' is {sym['size']:,} bytes of code",
            "detail": f"Large functions increase I-cache pressure and may indicate "
                      f"missing -Os or opportunities to split into smaller units."
        })

    # 5. Undefined symbols (linker would have caught these, but nm shows them)
    undef = [s for s in symbols if s['type'] == 'undefined']
    if undef:
        warnings.append({
            "level": "info",
            "category": "Undefined Symbols",
            "message": f"{len(undef)} undefined symbol(s) referenced",
            "detail": "These are resolved by the linker from libraries. "
                      + ", ".join(s['name'] for s in undef[:8])
                      + ("..." if len(undef) > 8 else "")
        })

    return warnings


def compute_startup_cost(ld_sections, elf_sections):
    """Compute flash→RAM copy sizes for startup cost analysis."""
    items = []
    total_copy = 0
    total_zero = 0
    for sec in ld_sections:
        es = elf_sections.get(sec['name'], {})
        sz = es.get('size', 0)
        if sz == 0: continue
        if sec['noload']:
            total_zero += sz
            items.append({"section": sec['name'], "type": "zeroed", "size": sz,
                          "vma": sec['vma'], "lma": sec['lma']})
        elif sec['lma']:
            total_copy += sz
            items.append({"section": sec['name'], "type": "copied", "size": sz,
                          "vma": sec['vma'], "lma": sec['lma']})
    return {"items": items, "total_copy": total_copy, "total_zero": total_zero}


